package com.boshi.entity;

public class Code {

  private Integer id;
  private String phone;
  private String Code;

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public String getPhone() {
    return phone;
  }

  public void setPhone(String phone) {
    this.phone = phone;
  }

  public String getCode() {
    return Code;
  }

  public void setCode(String code) {
    Code = code;
  }
}
